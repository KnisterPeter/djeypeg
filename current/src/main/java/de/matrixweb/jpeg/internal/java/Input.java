package de.matrixweb.jpeg.internal.java;

/**
 * @author markusw
 */
public class Input {

  private String chars;

  /**
   * @param chars
   */
  public Input(final String chars) {
    this.chars = chars;
  }

  /**
   * @return the chars
   */
  public String getChars() {
    return this.chars;
  }

  /**
   * @param chars
   *          the chars to set
   */
  public void setChars(final String chars) {
    this.chars = chars;
  }

}
